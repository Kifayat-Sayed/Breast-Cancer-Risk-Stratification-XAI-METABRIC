def create_risk(row):
    months = row["Overall Survival (Months)"]
    status = row["Overall Survival Status"]

    if status == "1:DECEASED" and months < 60:
        return "High Risk"

    elif status == "0:LIVING" and months > 120:
        return "Low Risk"

    else:
        return "Intermediate Risk"

